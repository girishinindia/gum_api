-- ============================================================
-- Table: categories
-- Purpose: Master lookup for course categories
-- ============================================================
-- Root-level grouping for courses
-- Hierarchy: Category → Sub-Category → Course
-- ============================================================


CREATE TABLE categories (

    -- ── Primary Key ──
    id                      BIGINT          GENERATED ALWAYS AS IDENTITY PRIMARY KEY,

    -- ── Identity ──
    code                    CITEXT          NOT NULL,
    slug                    CITEXT          NOT NULL,

    -- ── Display ──
    display_order           SMALLINT        DEFAULT 0,
    icon_url                TEXT,
    image_url               TEXT,

    -- ── Flags ──
    is_new                  BOOLEAN         NOT NULL DEFAULT FALSE,
    new_until               DATE,                                   -- auto-expire "new" badge after this date

    -- ── Audit ──
    created_by              BIGINT          REFERENCES users(id) ON DELETE SET NULL,
    updated_by              BIGINT          REFERENCES users(id) ON DELETE SET NULL,

    -- ── Status ──
    is_active               BOOLEAN         NOT NULL DEFAULT TRUE,
    is_deleted              BOOLEAN         NOT NULL DEFAULT FALSE,

    -- ── Timestamps ──
    created_at              TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at              TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at              TIMESTAMPTZ,

    -- ── Unique Constraints ──
    CONSTRAINT uq_categories_code UNIQUE (code),
    CONSTRAINT uq_categories_slug UNIQUE (slug)
);


-- ── Indexes ──

CREATE INDEX idx_categories_code ON categories (code)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_categories_slug ON categories (slug)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_categories_display_order ON categories (display_order)
    WHERE is_deleted = FALSE AND is_active = TRUE;

CREATE INDEX idx_categories_active ON categories (is_active)
    WHERE is_deleted = FALSE;


-- ── Full-Text Search Indexes (pg_trgm) ──

CREATE INDEX idx_categories_content_trgm
    ON categories
    USING GIN ((COALESCE(code::TEXT, '') || ' ' || COALESCE(slug::TEXT, '')) gin_trgm_ops);


-- ── Trigger: auto-slug ──
CREATE TRIGGER trg_categories_slug
    BEFORE INSERT OR UPDATE ON categories
    FOR EACH ROW
    EXECUTE FUNCTION fn_auto_slug('code');

-- ── Trigger: updated_at ──
CREATE TRIGGER trg_categories_updated_at
    BEFORE UPDATE ON categories
    FOR EACH ROW
    EXECUTE FUNCTION fn_update_updated_at_column();


