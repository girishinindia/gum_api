-- ============================================================
-- Table: sub_categories
-- Purpose: Master lookup for course sub-categories
-- ============================================================
-- FK: category_id → categories (RESTRICT)
-- Hierarchy: Category → Sub-Category → Course
-- ============================================================


CREATE TABLE sub_categories (

    -- ── Primary Key ──
    id                      BIGINT          GENERATED ALWAYS AS IDENTITY PRIMARY KEY,

    -- ── Relationships ──
    category_id             BIGINT          NOT NULL REFERENCES categories(id) ON DELETE RESTRICT,

    -- ── Identity ──
    code                    CITEXT          NOT NULL,
    slug                    CITEXT          NOT NULL,

    -- ── Display ──
    display_order           SMALLINT        DEFAULT 0,
    icon_url                TEXT,
    image_url               TEXT,

    -- ── Flags ──
    is_new                  BOOLEAN         NOT NULL DEFAULT FALSE,
    new_until               DATE,

    -- ── Audit ──
    created_by              BIGINT          REFERENCES users(id) ON DELETE SET NULL,
    updated_by              BIGINT          REFERENCES users(id) ON DELETE SET NULL,

    -- ── Status ──
    is_active               BOOLEAN         NOT NULL DEFAULT TRUE,
    is_deleted              BOOLEAN         NOT NULL DEFAULT FALSE,

    -- ── Timestamps ──
    created_at              TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at              TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at              TIMESTAMPTZ,

    -- ── Unique Constraints ──
    CONSTRAINT uq_sub_categories_category_slug UNIQUE (category_id, slug)
);


-- ── Indexes ──

CREATE INDEX idx_sub_categories_category ON sub_categories (category_id)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_sub_categories_code ON sub_categories (code)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_sub_categories_slug ON sub_categories (slug)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_sub_categories_display_order ON sub_categories (category_id, display_order)
    WHERE is_deleted = FALSE AND is_active = TRUE;

CREATE INDEX idx_sub_categories_active ON sub_categories (is_active)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_sub_categories_category_active ON sub_categories (category_id, is_active)
    WHERE is_deleted = FALSE;


-- ── Full-Text Search Indexes (pg_trgm) ──

CREATE INDEX idx_sub_categories_content_trgm
    ON sub_categories
    USING GIN ((COALESCE(code::TEXT, '') || ' ' || COALESCE(slug::TEXT, '')) gin_trgm_ops);


-- ── Trigger: auto-slug ──
CREATE TRIGGER trg_sub_categories_slug
    BEFORE INSERT OR UPDATE ON sub_categories
    FOR EACH ROW
    EXECUTE FUNCTION fn_auto_slug('code');

-- ── Trigger: updated_at ──
CREATE TRIGGER trg_sub_categories_updated_at
    BEFORE UPDATE ON sub_categories
    FOR EACH ROW
    EXECUTE FUNCTION fn_update_updated_at_column();


