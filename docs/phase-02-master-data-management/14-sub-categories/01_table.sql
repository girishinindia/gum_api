-- ============================================================
-- Table: sub_category_translations
-- Purpose: Multi-language content for sub-categories
-- ============================================================
-- FK: sub_category_id → sub_categories (RESTRICT)
-- FK: language_id → languages (RESTRICT)
-- UNIQUE: one translation per sub-category per language
-- ============================================================


CREATE TABLE sub_category_translations (

    -- ── Primary Key ──
    id                      BIGINT          GENERATED ALWAYS AS IDENTITY PRIMARY KEY,

    -- ── Relationships ──
    sub_category_id         BIGINT          NOT NULL REFERENCES sub_categories(id) ON DELETE RESTRICT,
    language_id             BIGINT          NOT NULL REFERENCES languages(id) ON DELETE RESTRICT,

    -- ── Content ──
    name                    CITEXT          NOT NULL,
    description             TEXT,
    is_new_title            TEXT,

    -- ── Media ──
    icon                    TEXT,
    image                   TEXT,

    -- ── Tags ──
    tags                    JSONB           DEFAULT '[]'::JSONB,

    -- ── SEO: Meta ──
    meta_title              TEXT,
    meta_description        TEXT,
    meta_keywords           TEXT,
    canonical_url           TEXT,

    -- ── SEO: Open Graph ──
    og_site_name            TEXT,
    og_title                TEXT,
    og_description          TEXT,
    og_type                 TEXT,
    og_image                TEXT,
    og_url                  TEXT,

    -- ── SEO: Twitter Card ──
    twitter_site            TEXT,
    twitter_title           TEXT,
    twitter_description     TEXT,
    twitter_image           TEXT,
    twitter_card            TEXT            DEFAULT 'summary_large_image',

    -- ── SEO: Other ──
    robots_directive        TEXT            DEFAULT 'index,follow',
    focus_keyword           TEXT,

    -- ── Full-Text Search ──
    search_vector           TSVECTOR        GENERATED ALWAYS AS (
        setweight(to_tsvector('simple', coalesce(name, '')), 'A') ||
        setweight(to_tsvector('simple', coalesce(description, '')), 'B') ||
        setweight(to_tsvector('simple', coalesce(meta_title, '')), 'A') ||
        setweight(to_tsvector('simple', coalesce(meta_description, '')), 'B') ||
        setweight(to_tsvector('simple', coalesce(focus_keyword, '')), 'A') ||
        setweight(to_tsvector('simple', coalesce(tags::TEXT, '')), 'D')
    ) STORED,

    -- ── Structured Data ──
    structured_data         JSONB           DEFAULT '[]'::JSONB,

    -- ── Audit ──
    created_by              BIGINT          REFERENCES users(id) ON DELETE SET NULL,
    updated_by              BIGINT          REFERENCES users(id) ON DELETE SET NULL,

    -- ── Status ──
    is_active               BOOLEAN         NOT NULL DEFAULT TRUE,
    is_deleted              BOOLEAN         NOT NULL DEFAULT FALSE,

    -- ── Timestamps ──
    created_at              TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at              TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    deleted_at              TIMESTAMPTZ,

    -- ── Unique Constraint ──
    CONSTRAINT uq_sub_category_translations_sub_cat_lang UNIQUE (sub_category_id, language_id)
);


-- ── Indexes ──

CREATE INDEX idx_sub_category_translations_sub_cat ON sub_category_translations (sub_category_id)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_sub_category_translations_language ON sub_category_translations (language_id)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_sub_category_translations_name ON sub_category_translations USING gin (name gin_trgm_ops)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_sub_category_translations_search ON sub_category_translations USING gin (search_vector);

CREATE INDEX idx_sub_category_translations_tags ON sub_category_translations USING gin (tags)
    WHERE is_deleted = FALSE;

CREATE INDEX idx_sub_category_translations_active ON sub_category_translations (is_active)
    WHERE is_deleted = FALSE;


-- ── Full-Text Search Indexes (pg_trgm) ──

CREATE INDEX idx_sub_category_translations_content_trgm
    ON sub_category_translations
    USING GIN ((COALESCE(name::TEXT, '') || ' ' || COALESCE(description::TEXT, '')) gin_trgm_ops);


-- ── Trigger: updated_at ──
-- ── Trigger: backfill parent slug from translation name ──
CREATE TRIGGER trg_sub_category_translations_backfill_slug
    AFTER INSERT ON sub_category_translations
    FOR EACH ROW
    EXECUTE FUNCTION fn_backfill_slug('sub_categories', 'sub_category_id', 'name');

CREATE TRIGGER trg_sub_category_translations_updated_at
    BEFORE UPDATE ON sub_category_translations
    FOR EACH ROW
    EXECUTE FUNCTION fn_update_updated_at_column();


